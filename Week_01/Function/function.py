def sum(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

sum_result = sum(5, 3)
subtract_result = subtract(5, 3)
multiply_result = multiply(5, 3)
divide_result = divide(5, 3)

print("Sum:", sum_result)
print("Subtract:", subtract_result)
print("Multiply:", multiply_result)
print("Divide:", divide_result)